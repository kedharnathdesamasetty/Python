num1 = 2.5
num2 = 5.0

# Addition
result1 = num1+num2
print("Addition:", result1)

# Subtraction
result2 = num1-num2
print("subtraction:", result2)

# division
result3 = num1-num2
print("division:", result3)

# Multiplication
result4 = num1*num2
print("multiplication:", result4)

